class Group {
    constructor (
        id,
        groupName,
        groupType,
        groupUsername,
        groupDescription,
        image
    ) {
        this.id = id;
        this.groupName = groupName;
        this.groupType = groupType;
        this.groupUsername = groupUsername;
        this.groupDescription = groupDescription;
        this.image = image;
    }
}

export default Group;